/**
 * El script inline de layout.tsx ya decidió la clase .dark antes del primer
 * pintado -aquí solo hay que leerla, no volver a decidir nada-. El icono
 * mostrado es el destino del clic, no el estado actual: luna en modo claro
 * ("pulsa para oscurecer"), sol en modo oscuro ("pulsa para aclarar").
 */
export function ThemeToggle() {
  const [isDark, setIsDark] = useState(false);

  useEffect(() => {
    setIsDark(document.documentElement.classList.contains('dark'));
  }, []);

  function toggle() {
    const next = !isDark;
    setIsDark(next);
    document.documentElement.classList.toggle('dark', next);
    localStorage.setItem('theme', next ? 'dark' : 'light');
  }

  return (
    <button
      type="button"
      onClick={toggle}
      aria-label={isDark ? 'Cambiar a modo claro' : 'Cambiar a modo oscuro'}
      title={isDark ? 'Modo claro' : 'Modo oscuro'}
      className="btn-ghost px-2"
    >
      {isDark ? <SunIcon /> : <MoonIcon />}
    </button>
  );
}
